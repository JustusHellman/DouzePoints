export const getMemberLabel = (count: number) => {
  if (count === 1) return "Solo";
  if (count === 2) return "Duo";
  return count.toString();
};

export const getGenreParent = (genre: string) => {
  return GENRE_PARENT_MAP[genre] || "Other";
};
